#!/usr/bin/env node
/**
 * Fetches the latest published blog posts at build time and writes
 * `src/data/journal-posts.ts` so the landing Journal section renders
 * crawlable HTML without client-side fetching.
 */

import { writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(__dirname, "..");
const outFile = resolve(projectRoot, "src/data/journal-posts.ts");

const DEFAULT_API = "https://mobile-api.novasafe.io";
const apiBase = (process.env.VITE_API_URL || process.env.API_URL || DEFAULT_API).replace(/\/$/, "");
const blogBase = `${apiBase}/api/v1/blog`;

const FALLBACK = [
  {
    slug: "why-most-password-managers-still-require-trust",
    category: "Security",
    title: "Why Most Password Managers Still Require Trust",
    excerpt:
      "Most password managers ask you to trust their servers. NovaSafe is built on zero-knowledge security so your vault stays private — even from us.",
    readingTimeMinutes: 6,
  },
  {
    slug: "passwords-vs-passkeys-whats-actually-safer",
    category: "Research",
    title: "Passwords vs Passkeys: What's Actually Safer?",
    excerpt:
      "Passkeys reduce phishing and credential theft, but migration matters. A practical look at when passkeys win and how to adopt them safely.",
    readingTimeMinutes: 5,
  },
  {
    slug: "understanding-zero-knowledge-security",
    category: "Engineering",
    title: "Understanding Zero-Knowledge Security",
    excerpt:
      "How encryption happens on your device before data ever reaches the cloud — and why that architecture is the foundation of modern password security.",
    readingTimeMinutes: 7,
  },
];

function readingTimeMinutes(text) {
  const words = (text || "").trim().split(/\s+/).filter(Boolean).length;
  return Math.max(1, Math.ceil(words / 200));
}

function escapeString(value) {
  return JSON.stringify(value ?? "");
}

async function fetchJournalPosts() {
  try {
    const [postsRes, categoriesRes] = await Promise.all([
      fetch(`${blogBase}/posts?perPage=3`, { headers: { Accept: "application/json" } }),
      fetch(`${blogBase}/categories`, { headers: { Accept: "application/json" } }).catch(() => null),
    ]);

    if (!postsRes.ok) throw new Error(`posts ${postsRes.status}`);

    const postsJson = await postsRes.json();
    const categoriesJson = categoriesRes?.ok ? await categoriesRes.json() : { data: [] };
    const catMap = new Map((categoriesJson.data || []).map((c) => [c.id, c.name]));
    const posts = (postsJson.data || []).slice(0, 3);

    if (!posts.length) return FALLBACK;

    const mapped = posts.map((p) => {
      const excerpt =
        p.excerpt?.trim() ||
        p.seo_description?.trim() ||
        (p.content_markdown || "").replace(/[#*_`[\]]/g, "").slice(0, 160).trim() + "…";
      const category = p.category_id ? catMap.get(p.category_id) || "Journal" : "Journal";

      return {
        slug: p.slug,
        category,
        title: p.title,
        excerpt,
        readingTimeMinutes: readingTimeMinutes(p.content_markdown || excerpt),
      };
    });

    if (mapped.length >= 3) return mapped.slice(0, 3);

    const seen = new Set(mapped.map((p) => p.slug));
    for (const fallback of FALLBACK) {
      if (mapped.length >= 3) break;
      if (!seen.has(fallback.slug)) {
        mapped.push(fallback);
        seen.add(fallback.slug);
      }
    }

    return mapped;
  } catch (err) {
    console.warn(`[journal] API fetch failed (${err.message}) — using fallback posts`);
    return FALLBACK;
  }
}

function renderModule(posts) {
  const serialized = posts
    .map(
      (p) => `  {
    slug: ${escapeString(p.slug)},
    category: ${escapeString(p.category)},
    title: ${escapeString(p.title)},
    excerpt: ${escapeString(p.excerpt)},
    readingTimeMinutes: ${p.readingTimeMinutes},
  }`,
    )
    .join(",\n");

  return `// Auto-generated by scripts/generate-journal-posts.mjs — do not edit by hand.
export type JournalPost = {
  slug: string;
  category: string;
  title: string;
  excerpt: string;
  readingTimeMinutes: number;
};

export const FALLBACK_JOURNAL_POSTS: JournalPost[] = [
${FALLBACK.map(
  (p) => `  {
    slug: ${escapeString(p.slug)},
    category: ${escapeString(p.category)},
    title: ${escapeString(p.title)},
    excerpt: ${escapeString(p.excerpt)},
    readingTimeMinutes: ${p.readingTimeMinutes},
  }`,
).join(",\n")}
];

/** Latest published posts — embedded at build time for SEO/crawlers. */
export const JOURNAL_POSTS: JournalPost[] = [
${serialized}
];
`;
}

const posts = await fetchJournalPosts();
writeFileSync(outFile, renderModule(posts), "utf8");
console.log(`[journal] wrote ${posts.length} posts → src/data/journal-posts.ts (api: ${apiBase})`);
